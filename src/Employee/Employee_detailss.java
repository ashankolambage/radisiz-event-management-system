    private void autofill() {
        try {
            ResultSet rs = DB.getCon().createStatement().executeQuery("SELECT * FROM employee WHERE reg_no = '"+jTextField1.getText()+"'");
            while (rs.next()){
                jDateChooser2.setDate(rs.getDate("registration_date"));
                jComboBox1.setSelectedIndex(rs.getInt("title"));
                jTextField2.setText(rs.getString("name"));
                jDateChooser1.setDate(rs.getDate("date_of_birth"));
                if (rs.getString("gender").equals("Male")){
                    jRadioButton1.setSelected(true);
                    jRadioButton2.setSelected(false);
                }else{
                    jRadioButton2.setSelected(true);
                    jRadioButton1.setSelected(false);
                }
                jTextField9.setText(rs.getString("nic"));
                jTextField4.setText(rs.getString("mobile"));
                jTextField5.setText(rs.getString("email"));
                jTextField6.setText(rs.getString("no"));
                jTextField7.setText(rs.getString("street"));
                jTextField8.setText(rs.getString("city"));
                jTextArea1.setText(rs.getString("working_description"));
                if ((rs.getString("state")).equals("Not_Remove")){
                    jButton4.setText("Dismiss");
                }else{
                    jButton4.setText("Undo Dismiss");
                }
                jTextField2.requestFocus();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void setimage() {
        try {
            ResultSet rs = DB.getCon().createStatement().executeQuery("SELECT image FROM employee WHERE reg_no = '"+jTextField1.getText()+"'");
            String s = "";
            while (rs.next()) {
                s  = rs.getString("image");
            }
            if (s != null){
                s = s.replace("/", "//");
                InputStream is = new FileInputStream(s);
                BufferedImage read = ImageIO.read(is);
                Image icon = read.getScaledInstance(jLabel18.getWidth(), jLabel18.getHeight(), Image.SCALE_SMOOTH);
                jLabel18.setIcon(new ImageIcon(icon));
                jButton8.setText("Change");
            }else{
                jLabel18.setIcon(null);
                jButton8.setText("Upload");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
