    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int ii = 0;
        if ((jDateChooser1.getDate() == null) &(jDateChooser2.getDate() != null)){
            ii = 1;
        }else if ((jDateChooser1.getDate() != null) &(jDateChooser2.getDate() == null)){
            ii = 1;
        }
        if (ii == 1){
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(RootPanel, "Please Select Next Date");
        }else{
            try {
            id = jTextField1.getText();
            ResultSet rs = null;
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            if (jTextField1.getText().equals("") & jDateChooser1.getDate() != null & jDateChooser2.getDate() != null){
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                Date d = jDateChooser1.getDate();
                String date1 = sdf.format(d);
                d = jDateChooser2.getDate();
                String date2 = sdf.format(d);
                rs = DB.getCon().createStatement().executeQuery("SELECT * FROM employee WHERE registration_date BETWEEN '"+date1+"' AND '"+date2+"'");
            }else if ((!jTextField1.getText().equals("")) & jDateChooser1.getDate() != null & jDateChooser2.getDate() != null){
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                Date d = jDateChooser1.getDate();
                String date1 = sdf.format(d);
                d = jDateChooser2.getDate();
                String date2 = sdf.format(d);
                if (jTextField1.getText().length() >= 3){
                    String s = id.substring(0, 3);
                        if (s.equals("em_")){
                            rs = DB.getCon().createStatement().executeQuery("SELECT * FROM employee WHERE registration_date BETWEEN '"+date1+"' AND '"+date2+"' AND reg_no LIKE '"+jTextField1.getText()+"%'");
                        }else{
                            rs = DB.getCon().createStatement().executeQuery("SELECT * FROM employee WHERE registration_date BETWEEN '"+date1+"' AND '"+date2+"' AND name LIKE '%"+jTextField1.getText()+"%'");
                        }
                }else{
                    rs = DB.getCon().createStatement().executeQuery("SELECT * FROM employee WHERE registration_date BETWEEN '"+date1+"' AND '"+date2+"' AND name LIKE '%"+jTextField1.getText()+"%'");
                }
            }else if ((!jTextField1.getText().equals("")) & jDateChooser1.getDate() == null & jDateChooser2.getDate() == null){
                if (jTextField1.getText().length() >= 3){
                    String s = id.substring(0, 3);
                        if (s.equals("em_")){
                            rs = DB.getCon().createStatement().executeQuery("SELECT * FROM employee WHERE reg_no LIKE '"+jTextField1.getText()+"%'");
                        }else{
                            rs = DB.getCon().createStatement().executeQuery("SELECT * FROM employee WHERE name LIKE '%"+jTextField1.getText()+"%'");
                        }
                }else{
                    rs = DB.getCon().createStatement().executeQuery("SELECT * FROM employee WHERE name LIKE '%"+jTextField1.getText()+"%'");
                }
            }
            while (rs.next()){
                Vector v = new Vector();
                v.add(rs.getString("reg_no"));
                v.add(rs.getString("name"));
                v.add(rs.getString("gender"));
                v.add(rs.getString("nic"));
                v.add(rs.getString("mobile"));
                v.add(rs.getString("working_description"));
                v.add(rs.getString("registration_date"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        if (jTextField1.getText().isEmpty() & jDateChooser1.getDate() == null & jDateChooser2.getDate() == null){
            load_table();
        }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

