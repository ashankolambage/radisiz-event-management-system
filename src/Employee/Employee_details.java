    private void load_table() {

        ResultSet rs = null;
        try {
            rs = DB.getCon().createStatement().executeQuery("SELECT * FROM employee WHERE state = '"+"Not_Remove"+"'");
                if (!rs.next()){
                    Toolkit.getDefaultToolkit().beep();
                    JOptionPane.showMessageDialog(RootPanel, "<html><center>There Are No Registered Employees<br>Please Register Employees And</br> <br>Try Again</br></center></html>", "Information", JOptionPane.INFORMATION_MESSAGE);
                }else{
                    rs = DB.getCon().createStatement().executeQuery("SELECT * FROM employee WHERE state = '"+"Not_Remove"+"'");
                }
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(0);
                while (rs.next()){
                    Vector v = new Vector();
                    v.add(rs.getString("reg_no"));
                    v.add(rs.getString("name"));
                    v.add(rs.getString("gender"));
                    v.add(rs.getString("nic"));
                    v.add(rs.getString("mobile"));
                    v.add(rs.getString("working_description"));
                    v.add(rs.getString("registration_date"));
                    dtm.addRow(v);
                }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
