    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String s = new String(jPasswordField1.getPassword());
        String ss = new String(jPasswordField2.getPassword());
        
        if (jTextField1.getText().isEmpty()){
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(RootPane, "Please Enter User Name", "Error", JOptionPane.ERROR_MESSAGE);
            jTextField1.requestFocus();
        }else if (s.isEmpty()){
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(RootPane, "Please Enter Password", "Error", JOptionPane.ERROR_MESSAGE);
            jPasswordField1.requestFocus();
        }else if (ss.isEmpty()){
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(RootPane, "Please Confirm The Password", "Error", JOptionPane.ERROR_MESSAGE);
            jPasswordField2.requestFocus();
        }else{
            try {
                ResultSet rs = DB.getCon().createStatement().executeQuery("SELECT username FROM login WHERE username = '"+jTextField1.getText()+"'");
                if (rs.next()){
                    Toolkit.getDefaultToolkit().beep();
                        JOptionPane.showMessageDialog(RootPane, "User Name IS Olready Exists", "Error", JOptionPane.ERROR_MESSAGE);
                        jTextField1.setText("");
                        jTextField1.requestFocus();
                }else{
                    if (s.equals(ss)){
                        DB.getCon().createStatement().executeUpdate("INSERT INTO login VALUES ('"+jTextField1.getText()+"' , '"+s+"')");
                        Toolkit.getDefaultToolkit().beep();
                        JOptionPane.showMessageDialog(RootPane, "Done", "Create Account", JOptionPane.INFORMATION_MESSAGE);
                        clr();
                    }else{
                        Toolkit.getDefaultToolkit().beep();
                        JOptionPane.showMessageDialog(RootPane, "Password Doesn't Match", "Error", JOptionPane.ERROR_MESSAGE);
                        jPasswordField1.setText("");
                        jPasswordField2.setText("");
                        jPasswordField1.requestFocus();
                    }
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

