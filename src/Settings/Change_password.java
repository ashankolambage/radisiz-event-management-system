    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int i = 0;
        String p1 = new String(jPasswordField1.getPassword());
        String p2 = new String(jPasswordField2.getPassword());
        String p3 = new String(jPasswordField3.getPassword());
        if (p1.isEmpty()){
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(RootPane, "Please Enter The Current Password", "Error", JOptionPane.ERROR_MESSAGE);
            jPasswordField1.requestFocus();
            i = 1;
        }else if (p2.isEmpty()){
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(RootPane, "Please Enter New Password", "Error", JOptionPane.ERROR_MESSAGE);
            jPasswordField2.requestFocus();
            i = 1;
        }else if (p3.isEmpty()){
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(RootPane, "Please Confirm The Password", "Error", JOptionPane.ERROR_MESSAGE);
            jPasswordField3.requestFocus();
            i = 1;
        }else{
            try {
                ResultSet rs = DB.getCon().createStatement().executeQuery("SELECT password FROM login WHERE username = '"+jComboBox1.getSelectedItem()+"'");
                    while (rs.next()){
                        if (rs.getString("password").equals(p1)){
                            if (p2.equals(p3)){
                                DB.getCon().createStatement().executeUpdate("UPDATE login SET password = '"+p1+"' WHERE username = '"+jComboBox1.getSelectedItem()+"'");
                                Toolkit.getDefaultToolkit().beep();
                                JOptionPane.showMessageDialog(RootPane, "Done", "Create Account", JOptionPane.INFORMATION_MESSAGE);
                                jPasswordField1.setText("");
                                jPasswordField2.setText("");
                                jPasswordField3.setText("");
                                jPasswordField1.requestFocus();
                            }else{
                                Toolkit.getDefaultToolkit().beep();
                                JOptionPane.showMessageDialog(RootPane, "Password Doesn't Match", "Error", JOptionPane.ERROR_MESSAGE);
                                jPasswordField2.setText("");
                                jPasswordField3.setText("");
                                jPasswordField2.requestFocus();
                            }
                        }else{
                            Toolkit.getDefaultToolkit().beep();
                            JOptionPane.showMessageDialog(RootPane, "Password Is Incorrect", "Error", JOptionPane.ERROR_MESSAGE);
                            jPasswordField1.setText("");
                            jPasswordField1.requestFocus();
                        }
                    }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

