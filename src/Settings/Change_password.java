    private void loadcombo() {
        try {
            ResultSet rs = DB.getCon().createStatement().executeQuery("SELECT username FROM login");
            jComboBox1.removeAllItems();
            while (rs.next()){
                jComboBox1.addItem(rs.getString("username"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

