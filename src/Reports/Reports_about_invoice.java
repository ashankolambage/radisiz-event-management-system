    private void load() {
        try {
            mindate();
            is = "D://Final Final Viva//Rreports//Invoice//All//Events//Invoice All 1.jrxml";
            mm.put("st date" , d1);
            mm.put("end date" , d2);
            rs = DB.getCon().createStatement().executeQuery("SELECT this_receipt_amount FROM event_invoice WHERE date >= '"+d1+"' AND date <= '"+d2+"'");
            InputStream iss = new FileInputStream(is);
            JasperReport jr = JasperCompileManager.compileReport(iss);
            JasperPrint jp = JasperFillManager.fillReport(jr, mm, DB.getCon());
            JRViewer jrv = new JRViewer(jp);
            if (jTabbedPane1.getTabCount() == 1){
                jTabbedPane1.removeAll();
            }
            jTabbedPane1.add("Invoice", jrv);
            credit();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
