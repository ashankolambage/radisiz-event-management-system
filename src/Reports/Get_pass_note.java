    private void setlocation() {
        int fheight = this.getHeight();
        int fwidth = this.getWidth();
        
        int height = (int) Toolkit.getDefaultToolkit().getScreenSize().getHeight();
        int width = (int) Toolkit.getDefaultToolkit().getScreenSize().getWidth();
        
        width = ((width - fwidth) / 2);
        height = ((height - fheight) / 4);
        
        setLocation(width, height);
    }

    private void search() {
        Map mm = new HashMap();
        mm.put("Event_ID", jTextField2.getText());
        try {
            InputStream is = new FileInputStream("D://Final Final Viva//Rreports//Get Pass Note//Get Pass Note.jrxml");
            JasperReport jr = JasperCompileManager.compileReport(is);
            JasperPrint jp = JasperFillManager.fillReport(jr, mm, DB.getCon());
            JRViewer jrv = new JRViewer(jp);
            if (jTabbedPane1.getTabCount() == 1){
                jTabbedPane1.removeAll();
            }
            jTabbedPane1.add("Get Pass Note", jrv);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
