    private void autogen() {
        try {
            ResultSet rs = DB.getCon().createStatement().executeQuery("SELECT * FROM inventory_damaged_items");
                if (!rs.next()){
                    sid = "indm_00000001";
                }else{
                    rs = DB.getCon().createStatement().executeQuery("SELECT MAX(damaged_id) FROM inventory_damaged_items");
                    while (rs.next()){
                        String s = rs.getString("MAX(damaged_id)");
                        int i = Integer.parseInt(s.substring(5, 13));
                        i+=1;
                        NumberFormat formatter = new DecimalFormat("00000000");
                        sid = "indm_" + formatter.format(i);
                    }
                }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
