    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        int iii = 0;
        Border br = new LineBorder(Color.red);
        if (jTextField2.getText().isEmpty()){
            jTextField2.setBorder(br);
            iii = 1;
        }
        if (jTextArea1.getText().isEmpty()){
            jTextArea1.setBorder(br);
            iii = 1;
        }
        if (jTextField4.getText().isEmpty()){
            jTextField4.setBorder(br);
            iii = 1;
        }
        if (iii == 1){
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(RootPanel, "Complete These Textboxes", "Error", JOptionPane.ERROR_MESSAGE);
            jTextField2.requestFocus();
        }else{
            try {
                ResultSet rs = DB.getCon().createStatement().executeQuery("SELECT package_id FROM event_manager_package WHERE package_id = '"+jTextField1.getText()+"'");
                if (rs.first()){
                    JOptionPane.showMessageDialog(RootPanel, "Can Not Update This Package", "Error", JOptionPane.ERROR_MESSAGE);
                }else{
                    DB.getCon().createStatement().executeUpdate("UPDATE inventory_packages SET name = '"+jTextField2.getText()+"', description = '"+jTextArea1.getText()+"', rental_price = '"+jTextField4.getText()+"' WHERE package_id = '"+jTextField1.getText()+"'");
                    DB.getCon().createStatement().executeUpdate("DELETE FROM inventory_package_components WHERE inventory_packages_component_id = '"+jTextField1.getText()+"'");
                    int row = jTable1.getRowCount();
                    int i = 0;
                    i = 0;
                    if (row != 0){
                        while (i < row){
                            String table[] = {jTable1.getValueAt(i, 0).toString(),jTable1.getValueAt(i, 1).toString(), jTable1.getValueAt(i, 2).toString(), jTable1.getValueAt(i, 3).toString()};
                            DB.getCon().createStatement().executeUpdate("INSERT INTO inventory_package_components VALUES ('"+jTextField1.getText()+"','"+table[0]+"', '"+table[1]+"', '"+table[2]+"', '"+table[3]+"')");
                            i+=1;
                        }
                    }
                    }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        settot();
    }//GEN-LAST:event_jButton2ActionPerformed

