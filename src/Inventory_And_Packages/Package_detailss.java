    private void settot() {
        double tot = 0;
        int i = 0;
        int row = 0;
        row = jTable1.getRowCount();
        if (row != 0){
            while (i < row){
                int qt = Integer.parseInt(jTable1.getValueAt(i, 3).toString());
                double pr = Double.parseDouble(jTable1.getValueAt(i, 2).toString());
                double d1 = pr * qt;
                tot = tot + d1;
                i+=1;
            }
        }
        jTextField4.setText("" + tot);
    }
    
