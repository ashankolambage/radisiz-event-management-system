    private void autogen() {
        Date d = new Date();
        jDateChooser1.setDate(d);
        try {
            ResultSet rs = DB.getCon().createStatement().executeQuery("SELECT * FROM inventory_item");
                if (!rs.next()){
                    jTextField1.setText("init_00000001");
                }else{
                    rs = DB.getCon().createStatement().executeQuery("SELECT MAX(item_id) FROM inventory_item");
                    while (rs.next()){
                        String s = rs.getString("MAX(item_id)");
                        int i = Integer.parseInt(s.substring(5, 13));
                        i+=1;
                        NumberFormat formatter = new DecimalFormat("00000000");
                        jTextField1.setText("init_" + formatter.format(i));
                    }
                }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
