    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased

        ResultSet rs = null;
        try {
            id = jTextField1.getText();
            if (jTextField1.getText().length() >= 5){
                String s = id.substring(0, 5);
                if (s.equals("inpc_")){
                    rs = DB.getCon().createStatement().executeQuery("SELECT * FROM inventory_packages WHERE package_id LIKE '"+jTextField1.getText()+"%'");
                }else{
                    rs = DB.getCon().createStatement().executeQuery("SELECT * FROM inventory_packages WHERE name LIKE '%"+jTextField1.getText()+"%'");
                }
            }else{
                rs = DB.getCon().createStatement().executeQuery("SELECT * FROM inventory_packages WHERE name LIKE '%"+jTextField1.getText()+"%'");
            }
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while (rs.next()){
                Vector v = new Vector();
                v.add(rs.getString("package_id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("description"));
                v.add(rs.getString("date"));
                v.add(rs.getString("rental_price"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTextField1KeyReleased

