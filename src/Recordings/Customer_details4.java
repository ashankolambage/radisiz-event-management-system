    private void autofill() {
        jTextField1.setText(Rec_customer_details.id);
        jTextField3.requestFocus();
        try {
            ResultSet rs = DB.getCon().createStatement().executeQuery("SELECT * FROM recording_customer WHERE reg_no = '"+Rec_customer_details.id+"'");
            while (rs.next()){
                jDateChooser1.setDate(rs.getDate("date"));
                jComboBox1.setSelectedItem(rs.getString("title"));
                jTextField3.setText(rs.getString("name"));
                jTextField4.setText(rs.getString("position"));
                jTextField5.setText(rs.getString("company"));
                jTextField6.setText(rs.getString("no"));
                jTextField7.setText(rs.getString("street"));
                jTextField8.setText(rs.getString("city"));
                jTextField9.setText(rs.getString("nic"));
                if (rs.getString("gender").equals("Male")){
                    jRadioButton1.setSelected(true);
                    jRadioButton2.setSelected(false);
                }else{
                    jRadioButton2.setSelected(true);
                    jRadioButton1.setSelected(false);
                }
                jTextField10.setText(rs.getString("mobile"));
                jTextField11.setText(rs.getString("land"));
                jTextField12.setText(rs.getString("email"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
