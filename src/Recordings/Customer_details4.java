    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        try {
            String gender = "";
            Date d = jDateChooser1.getDate();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String dd = sdf.format(d);
            if (jRadioButton1.isSelected()){
                gender = "Male";
            }else{
                gender = "Female";
            }
            DB.getCon().createStatement().executeUpdate("UPDATE recording_customer SET date = '"+dd+"', title = '"+jComboBox1.getSelectedItem()+"', name = '"+jTextField3.getText()+"', position = '"+jTextField4.getText()+"', company = '"+jTextField5.getText()+"', no = '"+jTextField6.getText()+"', street = '"+jTextField7.getText()+"', city = '"+jTextField8.getText()+"', nic = '"+jTextField9.getText()+"', gender = '"+gender+"', mobile = '"+jTextField10.getText()+"', land = '"+jTextField11.getText()+"', email = '"+jTextField12.getText()+"' WHERE reg_no = '"+jTextField1.getText()+"'");
            this.dispose();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

