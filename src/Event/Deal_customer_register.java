    private void jTextField9KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField9KeyTyped

        int l = jTextField9.getText().length() + 1;
        int i = evt.getKeyChar();
        char c = evt.getKeyChar();
        if (l < 11){
            if ((l<=9)){
                if (!(c>= '0' & c<= '9')){
                    evt.consume();
                    if (i == 8){
                    }else if (i == 127) {
                    }else if (i == 10){
                    }else{
                        Toolkit.getDefaultToolkit().beep();
                    }
                }
            }else{
                if (c>= '0' & c<= '9'){
                    evt.consume();
                    if (i == 8){
                    }else if (i == 127) {
                    }else if (i == 10){
                    }else{
                        Toolkit.getDefaultToolkit().beep();
                    }
                }
                if (c == 'x'){
                }else if (c == 'X'){
                }else if (c == 'v'){
                }else if (c == 'V'){
                }else{
                    evt.consume();
                    if (i == 8){
                    }else if (i == 127) {
                    }else if (i == 10){
                    }else{
                        Toolkit.getDefaultToolkit().beep();
                    }
                }
            }
        }else{
            evt.consume();
            if (i == 8){
            }else if (i == 127) {
            }else if (i == 10){
            }else{
                Toolkit.getDefaultToolkit().beep();
            }
                
        }
    }//GEN-LAST:event_jTextField9KeyTyped

