    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int iii = 0;
        Border br = new LineBorder(Color.red);
        if (jRadioButton5.isSelected()){
            if (jTextField3.getText().isEmpty()){
                jTextField3.setBorder(br);
            iii = 1;
            }
            if (jTextField4.getText().isEmpty()){
                jTextField4.setBorder(br);
                iii = 1;
            }
            if (jDateChooser1.getDate() == null){
                jDateChooser1.setBorder(br);
                iii = 1;
            }
            if (jTextField11.getText().isEmpty()){
                jTextField11.setBorder(br);
                iii = 1;
            }
            if (jTextField12.getText().isEmpty()){
                jTextField12.setBorder(br);
                iii = 1;
            }
            if (jTextField13.getText().isEmpty()){
                jTextField13.setBorder(br);
                iii = 1;
            }
        }else{
            if (jTextField3.getText().isEmpty()){
                jTextField3.setBorder(br);
            iii = 1;
            }
            if (jTextField4.getText().isEmpty()){
                jTextField4.setBorder(br);
                iii = 1;
            }
            if (jDateChooser1.getDate() == null){
                jDateChooser1.setBorder(br);
                iii = 1;
            }
        }
        if (iii == 1){
            JOptionPane.showMessageDialog(RootPanel, "Complete These Textboxes", "Error", JOptionPane.ERROR_MESSAGE);
            jTextField3.requestFocus();
        }else{
            String type = "";
            Date d = new Date();
            SimpleDateFormat sdf1 = new SimpleDateFormat("yyyy-MM-dd");
            SimpleDateFormat sdf2 = new SimpleDateFormat("HH:mm:ss");
            String s1 = sdf1.format(d);
            String s2 = sdf2.format(d);
            if (jRadioButton4.isSelected()){
                type = "Cash";
                try {
                    DB.getCon().createStatement().executeUpdate("INSERT INTO pay_roll(slip_no,employee_reg_no,description,charge,payment_type,date,time,user) VALUES ('"+jTextField1.getText()+"','"+jComboBox1.getSelectedItem().toString()+"','"+jTextField3.getText()+"','"+jTextField4.getText()+"','"+type+"','"+s1+"','"+s2+"','"+Login.User_login.user+"')");
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }else{
                type = "Cheque";
                try {
                    DB.getCon().createStatement().executeUpdate("INSERT INTO pay_roll VALUES ('"+jTextField1.getText()+"','"+jComboBox1.getSelectedItem().toString()+"','"+jTextField3.getText()+"','"+jTextField4.getText()+"','"+type+"','"+jTextField11.getText()+"','"+jTextField12.getText()+"','"+jTextField13.getText()+"','"+s1+"','"+s2+"','"+Login.User_login.user+"')");
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            Vector v = new Vector();
            v.add(jTextField1.getText());
            v.add(jTextField3.getText());
            v.add(jTextField4.getText());
            v.add(s1);
            dtm.addRow(v);
            autogen();
            settot();
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField3.requestFocus();
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(RootPanel, "Saved Sucessful");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

