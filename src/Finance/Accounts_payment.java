    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            if (jTable1.getRowCount() != 0){
                if (jTable1.getSelectedRowCount() != 0){
                    String ss = jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString();
                    DB.getCon().createStatement().executeUpdate("DELETE FROM pay_roll WHERE slip_no = '"+ss+"'");
                    dtm.removeRow(jTable1.getSelectedRow());
                }else{
                    String ss = jTable1.getValueAt(jTable1.getRowCount() - 1, 0).toString();
                    DB.getCon().createStatement().executeUpdate("DELETE FROM pay_roll WHERE slip_no = '"+ss+"'");
                    dtm.removeRow(jTable1.getRowCount() - 1);
                }
            }
            settot();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

