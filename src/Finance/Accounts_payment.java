    private void loadcombo() {
        try {
            ResultSet rs = DB.getCon().createStatement().executeQuery("SELECT reg_no FROM employee WHERE state = '"+"Not_Remove"+"'");
            while (rs.next()){
                jComboBox1.addItem(rs.getString("reg_no"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadtable() {
        if (jComboBox1.getItemCount() != 0){
            try {
                ResultSet rs = DB.getCon().createStatement().executeQuery("SELECT * FROM pay_roll WHERE employee_reg_no = '"+jComboBox1.getSelectedItem().toString()+"'");
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(0);
                while (rs.next()){
                    Vector v = new Vector();
                    v.add(rs.getString("slip_no"));
                    v.add(rs.getString("description"));
                    v.add(rs.getString("charge"));
                    v.add(rs.getString("date"));
                    dtm.addRow(v);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

    private void autogen() {
        try {
            ResultSet rs = DB.getCon().createStatement().executeQuery("SELECT * FROM pay_roll");
                if (!(rs.next())){
                    jTextField1.setText("pr_00000001");
                }else{
                    rs = DB.getCon().createStatement().executeQuery("SELECT MAX(slip_no) FROM pay_roll");
                    while (rs.next()){
                        String s = rs.getString("MAX(slip_no)");
                        int i = Integer.parseInt(s.substring(3, 11));
                        i+=1;
                        NumberFormat formatter = new DecimalFormat("00000000");
                        jTextField1.setText("pr_" + formatter.format(i));
                    }
                }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void settot() {
        tot = 0;
        int row = jTable1.getRowCount();
        int i = 0;
        if (row != 0){
            while (i < row){
                double d1 = Double.parseDouble(jTable1.getValueAt(i, 2).toString());
                tot = tot + d1;
                i+=1;
            }
        }
        jTextField9.setText("" + tot);
    }

    private void setimage() {
        if (jComboBox1.getItemCount() != 0){
            try {
                ResultSet rs = DB.getCon().createStatement().executeQuery("SELECT * FROM employee WHERE reg_no = '"+jComboBox1.getSelectedItem().toString()+"'");
                while (rs.next()){
                    jTextField2.setText(rs.getString("name"));
                    if (!(rs.getString("image").isEmpty())){
                        String s  = rs.getString("image");
                        s = s.replace("/", "//");
                        InputStream is = new FileInputStream(s);
                        BufferedImage read = ImageIO.read(is);
                        Image icon = read.getScaledInstance(jLabel15.getWidth(), jLabel15.getHeight(), Image.SCALE_SMOOTH);
                        jLabel15.setIcon(new ImageIcon(icon));
                    }else{
                        jLabel15.setIcon(null);
                    }
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        
    }
